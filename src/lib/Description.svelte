<h2>What is this?</h2>
<p>
    This is an alternative way of counting the time of the day.<br>
    The usual format is 24 hour, but we always feel like 24 hours are not enough and request that extra hour of the day.
</p>

<h2>How it works?</h2>
<p>
    The math behind this is quite straighforward.<br>
    For a normal 24 hour per day, we will have:<br>
    <b>24 hours</b><br>
    60 * 24 = <b>1440 minutes</b><br>
    60 * 60 * 24 = <b>86,400 seconds</b><br>
    1000 * 60 * 60 * 24 = <b>86,400,000 milliseconds</b><br>
    <br>
    To get 25 hours per day with an unnoticeable change, reduce the number of milliseconds.<br>
    A second in 25-hour day will be <b>24/25 times</b> shorter, which is <b>960 ms.</b><br>
    While remaining the number of seconds in a minute and minutes in an hour, 25-hour day will have:<br>
    <b>25 hours</b><br>
    60 * 25 = <b>1500 minutes</b><br>
    60 * 60 * 25 = <b>90,000 seconds</b><br>
    960 * 60 * 60 * 25 = <b>86,400,000 milliseconds</b><br>
</p>

